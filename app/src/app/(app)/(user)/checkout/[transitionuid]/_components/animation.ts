import confetti from "canvas-confetti";

export const handleClick = () => {
    const end = Date.now() + 3 * 1000; // 3 seconds
    const colors = ["#B22234", "#1B3A6B", "#4A7C2F", "#F8FAFC"];
 
    const frame = () => {
      if (Date.now() > end) return;
 
      confetti({
        particleCount: 2,
        angle: 60,
        spread: 55,
        startVelocity: 60,
        origin: { x: 0, y: 0.5 },
        colors: colors,
      });
      confetti({
        particleCount: 2,
        angle: 120,
        spread: 55,
        startVelocity: 60,
        origin: { x: 1, y: 0.5 },
        colors: colors,
      });
 
      requestAnimationFrame(frame);
    };
 
    frame();
  };
